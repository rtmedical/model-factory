"""Mixed gold (PDDCA) + silver (TS pseudo-labels) source for optic-nerve datasets.

For Dataset054_OpticNerves_HN_CT we combine two sub-cohorts in one
training corpus:

  - 48 PDDCA-1.4.1 cases with **gold** manual optic-nerve contours
    (`structures/OpticNerve_{L,R}.nrrd`), exactly the cohort already in
    Dataset011_HN_PDDCA_Optic. Images at
    `<pddca_root>/PDDCA-1.4.1_part*/<case>/img.nrrd`.

  - 82 (or as many as the orbit-manifest lists) TotalSegmentator v2 cases
    whose study_type includes the orbits. Images at
    `<ts_v2_image_root>/<case>/ct.nii.gz`. Their optic-nerve **silver**
    labels are generated by `scripts/run_ts_optic_labeller.py` (TS task
    `head_glands_cavities`) and stored at
    `<ts_pseudo_root>/<case>/optic_nerve_{left,right}.nii.gz`.

`MixedOpticSource` returns one merged CaseRef list. The orchestrator
sees just `OpticNerve_L` and `OpticNerve_R` as canonical structures;
the source resolves each to the correct on-disk file per case via the
`source` metadata flag.

Spec source_constraints:

    "mixed_optic": {
        "pddca_root": "/in/pddca_extracted/by_archive",
        "pddca_version": "1.4.1",
        "ts_v2_image_root": "/factory/intermediate/Dataset001_TotalSeg_v2",
        "ts_orbit_manifest": "/factory/intermediate/ts_v2_orbit_cases.txt",
        "ts_pseudo_root": "/factory/intermediate/ts_pseudo_optic_hn",
    }
"""

from __future__ import annotations

import logging
from collections.abc import Sequence
from pathlib import Path

import SimpleITK as sitk

from .base import CaseRef, DatasetSource


log = logging.getLogger("mixed_optic")


# canonical structure name → silver-side TS file stem
_SILVER_FILE = {
    "OpticNerve_L": "optic_nerve_left",
    "OpticNerve_R": "optic_nerve_right",
}


class MixedOpticSource(DatasetSource):
    """Combine PDDCA gold cases with TS silver-labelled head-region cases."""

    source_type = "mixed_optic"

    def __init__(
        self,
        pddca_root: Path,
        pddca_version: str,
        ts_v2_image_root: Path,
        ts_orbit_manifest: Path,
        ts_pseudo_root: Path,
    ):
        self.pddca_root = Path(pddca_root)
        self.pddca_version = pddca_version
        self.ts_v2_image_root = Path(ts_v2_image_root)
        self.ts_orbit_manifest = Path(ts_orbit_manifest)
        self.ts_pseudo_root = Path(ts_pseudo_root)

    # ── discovery ─────────────────────────────────────────────────────

    def _pddca_part_dirs(self) -> list[Path]:
        return sorted(self.pddca_root.glob(f"PDDCA-{self.pddca_version}_part*"))

    def _discover_pddca(self, structures: Sequence[str]) -> list[CaseRef]:
        out: list[CaseRef] = []
        seen: set[str] = set()
        n_skip = 0
        for part_dir in self._pddca_part_dirs():
            for case_dir in sorted(part_dir.iterdir()):
                if not case_dir.is_dir() or not (case_dir / "img.nrrd").is_file():
                    continue
                cid = case_dir.name
                if cid in seen:
                    continue
                seen.add(cid)
                struct_dir = case_dir / "structures"
                label_paths: dict[str, Path] = {}
                missing = False
                for canon in structures:
                    nrrd = struct_dir / f"{canon}.nrrd"
                    if nrrd.is_file():
                        label_paths[canon] = nrrd
                    else:
                        missing = True
                        break
                if missing:
                    n_skip += 1
                    continue
                out.append(
                    CaseRef(
                        case_id=cid,
                        patient_id=cid,                   # PDDCA: 1 case = 1 patient
                        image_path=case_dir / "img.nrrd",
                        label_paths=label_paths,
                        metadata={"source": "pddca", "pddca_version": self.pddca_version},
                    )
                )
        log.info("PDDCA-%s gold: %d cases ok, %d skipped (missing structure)",
                 self.pddca_version, len(out), n_skip)
        return out

    def _discover_ts_silver(self, structures: Sequence[str]) -> list[CaseRef]:
        if not self.ts_orbit_manifest.is_file():
            log.warning("ts_orbit_manifest not found at %s — no silver cohort",
                        self.ts_orbit_manifest)
            return []
        case_ids = [
            line.strip() for line in self.ts_orbit_manifest.read_text().splitlines()
            if line.strip()
        ]
        out: list[CaseRef] = []
        n_skip = 0
        for cid in case_ids:
            ct_path = self.ts_v2_image_root / cid / "ct.nii.gz"
            silver_dir = self.ts_pseudo_root / cid
            if not ct_path.is_file() or not silver_dir.is_dir():
                n_skip += 1
                continue
            label_paths: dict[str, Path] = {}
            missing = False
            for canon in structures:
                fname = _SILVER_FILE.get(canon)
                if fname is None:
                    log.warning("MixedOpticSource: no silver mapping for %r", canon)
                    missing = True
                    break
                fpath = silver_dir / f"{fname}.nii.gz"
                if fpath.is_file():
                    label_paths[canon] = fpath
                else:
                    missing = True
                    break
            if missing:
                n_skip += 1
                continue
            out.append(
                CaseRef(
                    case_id=cid,
                    patient_id=cid,                       # TS v2: 1 case = 1 patient
                    image_path=ct_path,
                    label_paths=label_paths,
                    metadata={"source": "ts_silver", "ts_task": "head_glands_cavities"},
                )
            )
        log.info("TS-silver: %d cases ok, %d skipped (missing image or labels)",
                 len(out), n_skip)
        return out

    def discover(self, structures: Sequence[str]) -> list[CaseRef]:
        # Validate the requested structures are ones we know how to map.
        unknown = [s for s in structures if s not in _SILVER_FILE]
        if unknown:
            log.warning("MixedOpticSource: structures %s have no TS-silver "
                        "file mapping; only PDDCA cases will resolve them",
                        unknown)
        gold = self._discover_pddca(structures)
        silver = self._discover_ts_silver(structures)
        all_cases = gold + silver
        log.info("MixedOpticSource discover: %d total (%d gold + %d silver)",
                 len(all_cases), len(gold), len(silver))
        return all_cases

    # ── per-case loaders ──────────────────────────────────────────────

    def load_image(self, case: CaseRef) -> sitk.Image:
        # NRRD (PDDCA) and NIfTI (TS) both supported by sitk.ReadImage.
        return sitk.ReadImage(str(case.image_path))

    def load_mask(
        self,
        case: CaseRef,
        canonical_name: str,
        ref_image: sitk.Image,
    ) -> sitk.Image:
        mask_path = case.label_paths.get(canonical_name)
        if mask_path is None or not mask_path.is_file():
            raise KeyError(canonical_name)
        img = sitk.ReadImage(str(mask_path))
        if img.GetSize() != ref_image.GetSize():
            img = sitk.Resample(
                img, ref_image, sitk.Transform(),
                sitk.sitkNearestNeighbor, 0, sitk.sitkUInt8,
            )
        elif img.GetPixelID() != sitk.sitkUInt8:
            img = sitk.Cast(img, sitk.sitkUInt8)
        return img


__all__ = ["MixedOpticSource"]
