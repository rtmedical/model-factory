"""RTSTRUCT (specialist) + TotalSegmentator pseudo-labels (context) fused source.

For F4-style "tight specialist + TS context" datasets (D050-D053), we
want each case's multi-label mask to combine:
  - Manual / silver specialist contours from a TCIA RTSTRUCT cohort
    (e.g., PenileBulb, ProstateBed, SeminalVes from cancer-archive)
  - TS pseudo-label binary masks (e.g., urinary_bladder, iliac vessels,
    prostate) pre-generated by `scripts/run_ts_pelvis_labeller.py` into
    `<ts_root>/<case_id>/<structure>.nii.gz`.

The spec declares which structures come from which source via the
`tags` dict (the `source_constraints[fused].rtstruct_structures` and
`ts_structures` keys). The fused source picks the right backend per
mask request and falls back if a TS file is missing (logs and returns
an empty mask so the convert orchestrator skips the case via the
`empty_label` filter).

Required spec source_constraints:
    "fused": {
        # specialist side — same as RTStructDicomSource
        "rtstruct_root":  "/in/rtstruct/pelve/m_ct",
        "tcia_root":      "/in/tcia/pelve/m_ct",
        "series_csv":     "/in/tcia/pelve/m_ct/series.csv",
        # TS-context side
        "ts_root":        "/factory/intermediate/ts_pseudo_pelvis",
        # which structures live in which source
        "rtstruct_structures": ["PenileBulb"],
        "ts_structures":       {"UrinaryBladder": "urinary_bladder"},
    }
"""

from __future__ import annotations

import logging
from collections.abc import Sequence
from pathlib import Path

import SimpleITK as sitk

from .base import CaseRef, DatasetSource
from .rtstruct import RTStructDicomSource


log = logging.getLogger("rtstruct_ts_fused")


class RTStructTSFusedSource(DatasetSource):
    """Combine an RTSTRUCT-cohort specialist source with a TS pseudo-label
    directory. discover() returns cases that have ALL requested
    rtstruct structures; missing TS-context files are not fatal — they
    yield empty masks and the orchestrator handles those.
    """

    source_type = "fused"

    def __init__(
        self,
        rtstruct_root: Path,
        tcia_root: Path,
        series_csv: Path,
        ts_root: Path,
        rtstruct_structures: Sequence[str],
        ts_structures: dict[str, str],
    ):
        self._rt = RTStructDicomSource(
            rtstruct_root=Path(rtstruct_root),
            tcia_root=Path(tcia_root),
            series_csv=Path(series_csv),
        )
        self._ts_root = Path(ts_root)
        self._rtstruct_structures = set(rtstruct_structures)
        # canonical_name → TS file stem (e.g., "UrinaryBladder" → "urinary_bladder")
        self._ts_structures = dict(ts_structures)

    def discover(self, structures: Sequence[str]) -> list[CaseRef]:
        # Filter spec structures down to the RTSTRUCT ones for discovery;
        # the cases must have all of those. TS structures are a per-case
        # post-step.
        rt_structs = [s for s in structures if s in self._rtstruct_structures]
        ts_structs = [s for s in structures if s in self._ts_structures]
        log.info(
            "fused discover: rt structs=%s ts structs=%s",
            rt_structs, ts_structs,
        )

        rt_cases = self._rt.discover(rt_structs)
        out: list[CaseRef] = []
        n_missing_ts = 0
        for c in rt_cases:
            case_dir = self._ts_root / c.case_id
            label_paths = dict(c.label_paths)  # keep rtstruct entries
            for canonical in ts_structs:
                ts_file = case_dir / f"{self._ts_structures[canonical]}.nii.gz"
                if ts_file.is_file():
                    label_paths[canonical] = ts_file
                else:
                    # mark as missing; the loader returns empty mask
                    label_paths[canonical] = ts_file  # path may not exist
                    n_missing_ts += 1
            out.append(
                CaseRef(
                    case_id=c.case_id,
                    patient_id=c.patient_id,
                    image_path=c.image_path,
                    label_paths=label_paths,
                    metadata={**c.metadata, "ts_root": str(self._ts_root)},
                )
            )
        log.info(
            "fused discover: %d cases (%d TS-mask file misses across cases — those return empty)",
            len(out), n_missing_ts,
        )
        return out

    def load_image(self, case: CaseRef) -> sitk.Image:
        return self._rt.load_image(case)

    def load_mask(
        self,
        case: CaseRef,
        canonical_name: str,
        ref_image: sitk.Image,
    ) -> sitk.Image:
        if canonical_name in self._rtstruct_structures:
            return self._rt.load_mask(case, canonical_name, ref_image)
        # TS-context structure
        if canonical_name not in self._ts_structures:
            log.warning("[%s] unknown structure %r in fused source", case.case_id, canonical_name)
            return self._empty_mask_like(ref_image)
        ts_path = case.label_paths.get(canonical_name)
        if ts_path is None or not ts_path.is_file():
            return self._empty_mask_like(ref_image)
        img = sitk.ReadImage(str(ts_path))
        if img.GetSize() != ref_image.GetSize():
            img = sitk.Resample(
                img, ref_image, sitk.Transform(),
                sitk.sitkNearestNeighbor, 0, sitk.sitkUInt8,
            )
        elif img.GetPixelID() != sitk.sitkUInt8:
            img = sitk.Cast(img, sitk.sitkUInt8)
        return img

    @staticmethod
    def _empty_mask_like(ref_image: sitk.Image) -> sitk.Image:
        empty = sitk.Image(ref_image.GetSize(), sitk.sitkUInt8)
        empty.CopyInformation(ref_image)
        return empty


__all__ = ["RTStructTSFusedSource"]
